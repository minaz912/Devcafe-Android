    /**
     * Represents an asynchronous login/registration task used to authenticate
     * the user.
     */
    public class UserLoginTask extends AsyncTask<Void, Void, Boolean> {

        private final String mUsername;
        private final String mPassword;

        UserLoginTask(String username, String password) {
            mUsername = username;
            mPassword = password;
        }

        @Override
        protected Boolean doInBackground(Void... params) {
            // TODO: attempt authentication against a network service.

            try {
                LinkedHashMap<String, String> userPassMap = new LinkedHashMap<>();
                userPassMap.put("username", mUsername);
                userPassMap.put("password", mPassword);

                Gson gson = new Gson();
                String request = gson.toJson(userPassMap);

                URL url = new URL(LOGIN_URI.toString());
                HttpURLConnection connection = (HttpURLConnection) url.openConnection();
                connection.setRequestMethod("POST");
                connection.setRequestProperty("Content-Type", "application/json");
                connection.setRequestProperty("Accept", "application/json");
                connection.setDoOutput(true);
                connection.connect();

                DataOutputStream dataOutputStream = new DataOutputStream(connection
                        .getOutputStream());
                dataOutputStream.writeBytes(request);
                dataOutputStream.flush();
                dataOutputStream.close();

                int responseCode = connection.getResponseCode();
                String responseMessage = connection.getResponseMessage();
                InputStream inputStream;
                if (responseCode != 200) {
                    inputStream = connection.getErrorStream();
                    BufferedReader reader = new BufferedReader(new InputStreamReader(inputStream));
                    StringBuilder sb = new StringBuilder();
                    String line;
                    while ((line = reader.readLine()) != null) {
                        sb.append(line).append("\n");
                    }
                    reader.close();
                    Log.d("RESPONSE ERRORS", sb.toString());

                } else {
                    inputStream = connection.getInputStream();
                    BufferedReader reader = new BufferedReader(new InputStreamReader(inputStream));
                    StringBuilder sb = new StringBuilder();
                    String line;
                    while ((line = reader.readLine()) != null) {
                        sb.append(line).append("\n");
                    }
                    reader.close();
                    JsonElement jsonElement = new JsonParser().parse(sb.toString());
                    JsonObject jsonObject = jsonElement.getAsJsonObject();
                    Log.d("RESPONSE TOKEN", String.valueOf(jsonObject.get("key")));
                    MainActivity.AUTH_TOKEN = String.valueOf(jsonObject.get("key"));
                }
                Log.d("RESPONSE CODE", String.valueOf(responseCode));
                Log.d("RESPONSE MESSAGE", responseMessage);
                // Simulate network access.
//                Thread.sleep(2000);
            } catch (IOException e) {
                e.printStackTrace();
            }
            
            // TODO: register the new account here.
            return true;
        }

        @Override
        protected void onPostExecute(final Boolean success) {
            mAuthTask = null;
            showProgress(false);

            if (success) {
                finish();
            } else {
                mPasswordView.setError(getString(R.string.error_incorrect_password));
                mPasswordView.requestFocus();
            }
        }

        @Override
        protected void onCancelled() {
            mAuthTask = null;
            showProgress(false);
        }
    }
